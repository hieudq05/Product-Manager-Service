    public Boolean checkUsersExists(JTable table){
        for( int i = 0 ; i < table.getRowCount() ; i++ ){
            if(table.getValueAt(i, 0).toString().equals(txtMaNhanVien.getText()))return true;
        }
        return false;
    }
        

